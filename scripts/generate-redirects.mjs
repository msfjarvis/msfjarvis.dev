#!/usr/bin/env node
/**
 * Generates public/_redirects for Cloudflare Pages.
 *
 * Run automatically as a `prebuild` step so the file is present when
 * `astro build` copies public/ into dist/.
 *
 * Sources of redirects:
 *   1. Weeknote old-URL migration: /posts/weeknotes-<id>/ → /weeknotes/<id>/
 *      Only for entries published before the migration cutoff (2026-05-01).
 *   2. Aliases in frontmatter: any path listed in the `aliases` field of a
 *      content file gets a 301 to that entry's canonical URL.
 */

import { readFileSync, writeFileSync, readdirSync, statSync } from "fs";
import { join, extname, basename, dirname } from "path";
import { fileURLToPath } from "url";
import yaml from "js-yaml";

const __dirname = dirname(fileURLToPath(import.meta.url));
const PROJECT_ROOT = join(__dirname, "..");

const WEEKNOTE_CUTOFF = new Date("2026-05-01");

// ---------------------------------------------------------------------------
// Frontmatter reader (YAML --- delimiters, as written by the migration script)
// ---------------------------------------------------------------------------

function parseFrontmatter(raw) {
  const match = raw.match(/^---\r?\n([\s\S]+?)\r?\n---/);
  if (!match) return {};
  try {
    return yaml.load(match[1]) ?? {};
  } catch {
    return {};
  }
}

// ---------------------------------------------------------------------------
// Collect entries from a content directory
// ---------------------------------------------------------------------------

function collectEntries(dir) {
  if (!statSync(dir, { throwIfNoEntry: false })?.isDirectory()) return [];
  return readdirSync(dir)
    .filter((name) => {
      // Each entry is now a directory containing index.mdx
      const fullPath = join(dir, name);
      return statSync(fullPath, { throwIfNoEntry: false })?.isDirectory();
    })
    .map((name) => {
      const id = name;
      const indexPath = join(dir, name, "index.mdx");
      const raw = readFileSync(indexPath, "utf8");
      const data = parseFrontmatter(raw);
      return { id, data };
    });
}

// ---------------------------------------------------------------------------
// Main
// ---------------------------------------------------------------------------

function main() {
  const lines = [
    "# Generated by scripts/generate-redirects.mjs — do not edit by hand.",
    "# Regenerated automatically on every build.",
    "",
  ];

  const postsDir = join(PROJECT_ROOT, "src", "content", "posts");
  const notesDir = join(PROJECT_ROOT, "src", "content", "notes");
  const weeknotesDir = join(PROJECT_ROOT, "src", "content", "weeknotes");

  const posts = collectEntries(postsDir);
  const notes = collectEntries(notesDir);
  const weeknotes = collectEntries(weeknotesDir);

  // ── 1. Weeknote old-URL migration ────────────────────────────────────────
  const oldWeeknotePaths = weeknotes
    .filter((w) => {
      const date = w.data.date ? new Date(w.data.date) : null;
      return date && date < WEEKNOTE_CUTOFF;
    })
    .sort((a, b) => a.id.localeCompare(b.id));

  if (oldWeeknotePaths.length > 0) {
    lines.push("# Weeknote URL migration: /posts/weeknotes-* → /weeknotes/*");
    for (const w of oldWeeknotePaths) {
      lines.push(`/posts/weeknotes-${w.id}/\t/weeknotes/${w.id}/\t301`);
    }
    lines.push("");
  }

  // ── 2. Alias redirects ───────────────────────────────────────────────────
  const allEntries = [
    ...posts.map((e) => ({ ...e, canonical: `/posts/${e.id}/` })),
    ...notes.map((e) => ({ ...e, canonical: `/notes/${e.id}/` })),
    ...weeknotes.map((e) => ({ ...e, canonical: `/weeknotes/${e.id}/` })),
  ].filter((e) => Array.isArray(e.data.aliases) && e.data.aliases.length > 0);

  if (allEntries.length > 0) {
    lines.push("# Alias redirects: old paths → canonical URLs");
    for (const entry of allEntries) {
      for (const alias of entry.data.aliases) {
        // Normalise: ensure leading slash and trailing slash
        const src = ("/" + String(alias).replace(/^\//, "")).replace(/\/?$/, "/");
        lines.push(`${src}\t${entry.canonical}\t301`);
      }
    }
    lines.push("");
  }

  // ── 3. Manually instantiated redirects ──────────────────────────────────────
  // Hugo used to generate these at /index.xml, they're now at /rss.xml, manually redirect old feed readers.
  lines.push(`/index.xml /rss.xml 301`);
  lines.push(`/notes/index.xml /notes/rss.xml 301`);

  const dest = join(PROJECT_ROOT, "public", "_redirects");
  writeFileSync(dest, lines.join("\n"), "utf8");

  const total = oldWeeknotePaths.length + allEntries.reduce((n, e) => n + e.data.aliases.length, 0);
  console.log(`✓ public/_redirects written (${total} redirect${total !== 1 ? "s" : ""})`);
}

main();
